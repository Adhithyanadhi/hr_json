          <h3>About Us</h3>
          <p>WHAT WE ARE..
            <ul>
            <li>We are a research platform which promotes Interdisciplinary Water Research.</li>
            
            <li>We aims at finding real time feasible solutions for major problems related to water and its links.</li>
            
            <li>We also plan to find suitable rectifications for existing policies related to water. The organisation focuses on sustainable management of natural resources for the adaptation in regions with a growing population and changing climate.</li>
            
            <li>We envision to contribute its part to Zero Poverty and End Hunger.</li>
            
            <li>We plans to generate awareness of the vitality of water and promote efficient conservation practices.</li>
            
            <li>We plan to bridge the government to the locals for the implementation of new f`rable policies and raising concern of the people for the unfavourable.</li>
            
            <li>We aim at developing a circular economy for water and conserving bio-diversity for a sustainable water management.</li>
            
            <li>We aim to redefine the principle of Integrated Water Resources Management. Lastly, the organisation envision a gender equal and status equitable management of water resources.</li>
          </ul>
          <br>
          <br>
          <br>
            <center>WHAT WE DO..</center>
            <br>
            <br>
            <ul>
            <li>We achieve our goals by sustained research and a participatory approach.</li>
            
            <li>We use state of the art technologies for identifying key parameters in problems related to water.</li>
            
            <li>We simulate realities based on valid historical data to develop a better understanding of the world we live in.</li>
            
            <li>We forecast scenarios and come out with adaptations for the most probabilistic scenario.</li>
            
            <li>We monitor real-time demands of water to forecast any disasters and also provide suitable solutions.</li>
            
            <li>We publicize the idea of water and bio diversity conservation, gender equality and status equity, sustainable management practices of water resources.</li>
          </ul>
